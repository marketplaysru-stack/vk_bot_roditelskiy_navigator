# ===== ВАРИАНТЫ ЭМОДЗИ (добавляются случайно в пост) =====
# ============================================================

EMOJIS_BLOCKS = {
    "header": ["👋", "🌟", "💡", "❤️", "✨", "🌈"],
    "tips": ["✅", "📌", "🔍", "🛠️", "💪", "🧠", "💡", "⚡"],
    "discussion": ["💬", "🗣️", "👥", "🤝", "💭"],
    "engagement": ["👇", "✍️", "📝", "🤔", "💬"],
}

def get_random_emojis(block_type, count=2):
    """Возвращает случайные эмодзи для указанного блока."""
    emojis = EMOJIS_BLOCKS.get(block_type, ["✨"])
    return " ".join(random.sample(emojis, min(count, len(emojis))))

