TOPIC_HASHTAGS = {
    "parenting": ["#родительство", "#дети", "#воспитание", "#семья", "#материнство", "#отцовство"],
    "psychology": ["#детскаяпсихология", "#эмоции", "#развитие", "#общение", "#отношения"],
    "education": ["#образование", "#школа", "#развитие", "#чтение", "#учёба"],
    "health": ["#здоровье", "#спорт", "#питание", "#режимдня", "#закалка"],
    "family": ["#семья", "#дети", "#любовь", "#счастье", "#уют"],
    "vacation": ["#каникулы", "#отпуск", "#выходной", "#путешествия", "#природа"],
}

DEFAULT_HASHTAGS = ["#родительство", "#дети", "#семья", "#воспитание", "#советыродителям"]

def get_hashtags(topic, count=5):
    """Подбирает хештеги по ключевым словам темы."""
    topic_lower = topic.lower()
    best_match = DEFAULT_HASHTAGS
    for key, tags in TOPIC_HASHTAGS.items():
        if key in topic_lower:
            best_match = tags
            break
    # Если выбраны дефолтные, но тема содержит ключевые слова — пробуем найти
    if best_match == DEFAULT_HASHTAGS:
        for key, tags in TOPIC_HASHTAGS.items():
            if any(word in topic_lower for word in ["воспитание", "ребёнок", "дети", "семья"]):
                best_match = tags
                break
    return random.sample(best_match, min(count, len(best_match)))

